  // Метод для получения упражнений по группам мышц
  List<Exercise> getExercisesByMuscleGroup(String muscleGroup) {
    return _allExercises.where((exercise) {
      return exercise.primaryMuscleGroups.contains(muscleGroup) ||
             exercise.secondaryMuscleGroups.contains(muscleGroup);
    }).toList();
  }

  // Метод для получения упражнений по оборудованию
  List<Exercise> getExercisesByEquipment(List<String> availableEquipment) {
    return _allExercises
        .where((exercise) => exercise.isAvailableWith(availableEquipment))
        .toList();
  }

  // Метод для получения упражнений с учетом ограничений по здоровью
  List<Exercise> getSafeExercises(List<String> healthRestrictions) {
    return _allExercises
        .where((exercise) => exercise.isSafeFor(healthRestrictions))
        .toList();
  }

  // Метод для поиска упражнений по имени
  List<Exercise> searchExercises(String query) {
    if (query.isEmpty) return _allExercises;
    
    final lowerQuery = query.toLowerCase();
    return _allExercises.where((exercise) {
      return exercise.name.toLowerCase().contains(lowerQuery) ||
             exercise.description.toLowerCase().contains(lowerQuery) ||
             exercise.primaryMuscleGroups.any((muscle) => 
                muscle.toLowerCase().contains(lowerQuery));
    }).toList();
  }

  // ============ ИСПРАВЛЕННЫЙ МЕТОД ============
  // Метод для получения упражнения по ID - ВСЕГДА ВОЗВРАЩАЕТ Exercise
  Exercise getExerciseById(String id) {
    try {
      return _allExercises.firstWhere((exercise) => exercise.id == id);
    } catch (e) {
      return Exercise.empty();  // ← ВСЕГДА ВОЗВРАЩАЕТ Exercise
    }
  }

  // Метод для получения нескольких случайных упражнений
  List<Exercise> getRandomExercises({
    required int count,
    ExerciseCategory? category,
    ExerciseDifficulty? difficulty,
    List<String>? excludeIds,
  }) {
    List<Exercise> filtered = _allExercises;
    
    if (category != null) {
      filtered = filtered.where((e) => e.categories.contains(category)).toList();
    }
    
    if (difficulty != null) {
      filtered = filtered.where((e) => e.difficulty == difficulty).toList();
    }
    
    if (excludeIds != null) {
      filtered = filtered.where((e) => !excludeIds.contains(e.id)).toList();
    }
    
    if (filtered.isEmpty) return [];
    
    filtered.shuffle();
    return filtered.take(count).toList();
  }

  // Метод для получения упражнений для разминки (по умолчанию 5)
  List<Exercise> getDefaultWarmup({int count = 5}) {
    final warmupExercises = getWarmupExercises();
    warmupExercises.shuffle();
    return warmupExercises.take(count).toList();
  }

  // Метод для получения упражнений для заминки (по умолчанию 5)
  List<Exercise> getDefaultCooldown({int count = 5}) {
    final cooldownExercises = getCooldownExercises();
    cooldownExercises.shuffle();
    return cooldownExercises.take(count).toList();
  }

