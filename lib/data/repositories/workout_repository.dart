  List<Exercise> findAlternativeExercises(
    String currentExerciseId,
    List<String> availableEquipment,
  ) {
    final currentExercise = getExerciseById(currentExerciseId);
    if (currentExercise.id.isEmpty) return [];

    return _allExercises.where((exercise) {
      if (exercise.id == currentExerciseId) return false;
      if (!exercise.isAvailableWith(availableEquipment)) return false;
      
      return exercise.primaryMuscleGroups.any((muscle) => 
          currentExercise.primaryMuscleGroups.contains(muscle));
    }).toList();
  }

